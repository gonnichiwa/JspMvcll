package board.model;

public class ReplyDTO {
	
	private int rpy_num;
	private String rpy_author;
	private String rpy_content;
	private String rpy_date;
	private String rpy_parent_num;
	
	
	public int getRpy_num() {
		return rpy_num;
	}
	public void setRpy_num(int rpy_num) {
		this.rpy_num = rpy_num;
	}
	public String getRpy_author() {
		return rpy_author;
	}
	public void setRpy_author(String rpy_author) {
		this.rpy_author = rpy_author;
	}
	public String getRpy_content() {
		return rpy_content;
	}
	public void setRpy_content(String rpy_content) {
		this.rpy_content = rpy_content;
	}
	public String getRpy_date() {
		return rpy_date;
	}
	public void setRpy_date(String rpy_date) {
		this.rpy_date = rpy_date;
	}
	public String getRpy_parent_num() {
		return rpy_parent_num;
	}
	public void setRpy_parent_num(String rpy_parent_num) {
		this.rpy_parent_num = rpy_parent_num;
	}
	
	

}
